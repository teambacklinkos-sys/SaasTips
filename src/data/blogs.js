export const getCategoryMeta = (id) =>
  categories.find((c) => c.id === id) || categories[0];

export const getRelatedPosts = (currentId, category, count = 3) => {
  const sameCat = blogs.filter((b) => b.id !== currentId && b.category === category);
  if (sameCat.length >= count) return sameCat.slice(0, count);
  const others = blogs.filter((b) => b.id !== currentId && b.category !== category);
  return [...sameCat, ...others].slice(0, count);
};
